    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int b = JOptionPane.showConfirmDialog(null, "Deseja alterar os dados do produto selecionado?");
        if (b == 0) {
            MnuInternoAltProduto tela = new MnuInternoAltProduto();
            getParent().add(tela);
            tela.setVisible(true);
            dispose();
        }
    
        else { 
            MnuInternoConsProduto tela = new MnuInternoConsProduto();
            getParent().add(tela);
            tela.setVisible(true);
            dispose();
    }

    }//GEN-LAST:event_btnAlterarActionPerformed

    private void cmpProcuraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cmpProcuraKeyTyped
        String numero = "1234567890!@#$%&*()";

        if (numero.contains(evt.getKeyChar() + "")) {
            evt.consume();
            this.cmpProcura.setBackground(Color.red);
        } else {
            this.cmpProcura.setBackground(Color.white);
        }
    }//GEN-LAST:event_cmpProcuraKeyTyped


